// 1007. Minimum Domino Rotations For Equal Row
// https://leetcode.com/problems/minimum-domino-rotations-for-equal-row/description/
// T.C.: O(n)
// S.C.: O(1)
/**
 * @param {number[]} tops
 * @param {number[]} bottoms
 * @return {number}
 */
var minDominoRotations = function (tops, bottoms) {
  const n = tops.length;
  const rotations = countRotations(tops[0], tops, bottoms);
  if (rotations !== -1) {
    return rotations;
  }
  return countRotations(bottoms[0], bottoms, tops);

  function countRotations(domino, tops, bottoms) {
    let topRotations = 0;
    let bottomRotations = 0;
    for (let i = 0; i < n; i++) {
      if (domino !== tops[i] && domino !== bottoms[i]) {
        return -1;
      }
      if (domino !== tops[i]) {
        topRotations++;
      }
      if (domino !== bottoms[i]) {
        bottomRotations++;
      }
    }
    return Math.min(topRotations, bottomRotations);
  }
};

