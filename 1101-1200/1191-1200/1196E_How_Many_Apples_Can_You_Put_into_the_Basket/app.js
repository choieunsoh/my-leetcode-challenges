// 1196. How Many Apples Can You Put into the Basket
// https://leetcode.com/problems/how-many-apples-can-you-put-into-the-basket/description/
// T.C.: O(n)
// S.C.: O(1)
/**
 * @param {number[]} weight
 * @return {number}
 */
var maxNumberOfApples = function (weight) {
  const apples = new Array(1001).fill(0);
  for (const apple of weight) {
    apples[apple]++;
  }

  let count = 0;
  let total = 0;
  for (let apple = 0; apple <= 1000; apple++) {
    if (apples[apple] === 0) continue;

    const take = Math.min(apples[apple], ((5000 - total) / apple) | 0);
    if (take === 0) continue;

    total += take * apple;
    count += take;
  }
  return count;
};

