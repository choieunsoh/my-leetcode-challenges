// 1926. Nearest Exit from Entrance in Maze
// https://leetcode.com/problems/nearest-exit-from-entrance-in-maze/
/**
 * @param {character[][]} maze
 * @param {number[]} entrance
 * @return {number}
 */
var nearestExit = function (maze, entrance) {
  const rows = maze.length;
  const cols = maze[0].length;
  const dir = [1, 0, -1, 0, 1];

  const [startRow, startCol] = entrance;
  maze[startRow][startCol] = '0';
  let queue = [[startRow, startCol]];
  let steps = 0;
  while (queue.length) {
    const nextQueue = [];
    for (let i = 0; i < queue.length; i++) {
      const [row, col] = queue[i];
      for (let j = 0; j < 4; j++) {
        const [nextRow, nextCol, nextStep] = [row + dir[j], col + dir[j + 1], steps + 1];
        if (!valid(nextRow, nextCol, '.')) continue;
        maze[nextRow][nextCol] = nextStep.toString();
        if (nextRow === 0 || nextRow === rows - 1 || nextCol === 0 || nextCol === cols - 1) {
          return nextStep;
        }
        nextQueue.push([nextRow, nextCol]);
      }
    }
    steps++;
    queue = nextQueue;
  }

  return -1;

  function valid(row, col) {
    if (row < 0 || row >= rows || col < 0 || col >= cols || maze[row][col] !== '.') return false;
    return true;
  }
};

