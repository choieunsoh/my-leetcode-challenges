// 1293. Shortest Path in a Grid with Obstacles Elimination
// https://leetcode.com/problems/shortest-path-in-a-grid-with-obstacles-elimination/description/
// T.C: O(n*m)
// S.C: O(n*m)
/**
 * @param {number[][]} grid
 * @param {number} k
 * @return {number}
 */
var shortestPath = function (grid, k) {
  let queue = [[0, 0, k]]; // row, col, remain
  let steps = 0;
  const dir = [1, 0, -1, 0, 1];
  const rows = grid.length;
  const cols = grid[0].length;
  const visited = Array.from({ length: rows }, () => new Array(cols).fill(-1));
  while (queue.length) {
    const newQueue = [];
    for (const [row, col, remain] of queue) {
      if (remain <= visited[row][col]) continue;

      if (row === rows - 1 && col === cols - 1) {
        return steps;
      }

      visited[row][col] = remain;

      const neighbors = getNeighbors(row, col, remain);
      newQueue.push(...neighbors);
    }
    steps++;
    queue = newQueue;
  }
  return -1;

  function getNeighbors(row, col, remain) {
    const neighbors = [];
    for (let i = 0; i < 4; i++) {
      const [newRow, newCol] = [row + dir[i], col + dir[i + 1]];
      const key = `${newRow}-${newCol}-${remain}`;
      if (newRow < 0 || newRow >= rows || newCol < 0 || newCol >= cols || remain <= visited[newRow][newCol]) continue;
      if (grid[newRow][newCol] === 1) {
        if (remain > 0) neighbors.push([newRow, newCol, remain - 1]);
      } else {
        neighbors.push([newRow, newCol, remain]);
      }
    }
    return neighbors;
  }
};

